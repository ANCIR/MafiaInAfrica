
(function(){

    var selector = '.mafrica-vis .hotspot';

    $(selector).click(function(e){
        var href = $(this).attr('href');
        var target = $(this).data('target');
        var fallback = "#nakumatt";
        var modalId = href || target || fallback;
        $(modalId).modal();
        e.preventDefault();
    });
}());


